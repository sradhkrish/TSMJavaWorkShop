public class SumOfOddNumberUsingFormula {
    public static void main(String[] args) {
        int sumOfEvenNumbers=findSum(10);
        System.out.println("Sum of first 10 odd number="+sumOfEvenNumbers);
    }


    private static int findSum(int n) {

        return n*n;
    }

}
